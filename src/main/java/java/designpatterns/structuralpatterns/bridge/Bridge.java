package java.designpatterns.structuralpatterns.bridge;

public class Bridge {

	/* Esse padrão de projeto permite que dividamos uma classe única que
	* tem múltiplas variantes da mesma funcionalidade em duas hierarquias, que
	* são as abstrações e as implementações. */

	/* Com isso, o código do cliente não será exposto para os detalhes
	* de implementação. */

	/* Dessa forma, também vamos introduzir novas abstrações e
	* implementações, tornando fácil a alteração delas em runtime. */

	/* Quando usar: */

	/* 1. Devemos usar esse padrão quando queremos organizar uma classe
	* monolítica que tem muitas variantes da mesma funcionalidade, por
	* exemplo, se a classe pode trabalhar com vários servidores de
	* banco de dados. */
}
